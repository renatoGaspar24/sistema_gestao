class Usuario {
  final String nome;
  final String cargo;
  final String telefone;
  final String turno;
  final double salario;
  final DateTime dataAdmissao;

  Usuario({
    required this.nome,
    required this.cargo,
    required this.telefone,
    required this.turno,
    required this.salario,
    required this.dataAdmissao,
  });
}
